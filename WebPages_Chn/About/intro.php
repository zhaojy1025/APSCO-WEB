					<h1>组织简介</h1>
					<p style="text-align: center;">
						<b><span style="font-family: 宋体; color: #3366cc; font-size: 14pt;"><strong>联合国教科文组织国际自然与文化遗产空间技术中心</strong></span></b>
					</p>
					<p style="text-align: center;">&nbsp;</p>
					<p>
						<span style="font-family: 宋体; font-size: 12pt;">&nbsp;&nbsp;&nbsp;
							国际自然与文化遗产空间技术中心（简称“<span>HIST</span>”）系联合国教科文组织（<span>UNESCO</span>）二类中心，由中国科学院于<span>2007</span>年<span>5</span>月提议建立，<span>2009</span>年<span>10</span>月获<span>UNESCO</span>第<span>35</span>届大会批准<span>,
						</span>并于<span>2011</span>年<span>4</span>月获国务院正式批准。<span>2011</span>年<span>7</span>月<span>24</span>日，中心在北京正式成立。这是<span>UNESCO</span>在全球设立的第一个基于空间技术的世界遗产研究机构，依托中国科学院遥感与数字地球研究所建设。
						</span>
					</p>
					<p style="text-align: center;">
						<span style="font-family: 宋体; font-size: 12pt;"><img
