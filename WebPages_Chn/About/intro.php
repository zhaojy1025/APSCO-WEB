						<b><span style="font-family: 宋体; font-size: 12pt;">国际项目合作与交流<span></span></span></b>
					</p>
					<p style="vertical-align: top;" align="left">
						<span style="font-family: 宋体; font-size: 12pt;">&nbsp;&nbsp;&nbsp;&nbsp;
							HIST</span><span style="font-family: 宋体; font-size: 12pt;">自成立以来，建立了有效的机制体制，组建了一支高水平的科研和管理团队。在教科文组织的框架下，编撰了《世界遗产遥感图集<span>——</span>中国篇》（中英文）；开展了“吴哥世界遗产环境遥感”、“全球变化对世界遗产影响空间精细观测与认知”、“基于对地观测的中意文化遗产地智能管理与试点应用研究”等国际合作项目；为亚、非<span>20</span>余国世界遗产和世界生物圈保护区管理者提供了<span>3</span>期空间技术培训；举办了“第四届国际遥感考古会议”；还开展了针对长城、大运河、丝绸之路、五大连池等遗产地和生物圈保护区的监测和应用研究。<span></span></span>
					</p>
