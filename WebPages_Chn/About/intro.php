					<p style="text-align: center;" align="center">
						<b><span style="font-family: 宋体; font-size: 12pt;">总体目标</span></b><b><span
							style="font-family: 宋体; font-size: 12pt;"></span></b>
					</p>
					<p style="text-align: left; text-indent: 28.3pt;">
						<span style="font-family: 宋体; font-size: 12pt;">HIST</span><span
							style="font-family: 宋体; font-size: 12pt;">旨在利用空间技术，</span><span
							style="font-family: 宋体; font-size: 12pt;">为<span>UNESCO</span>及其成员国在世界遗产和世界生物圈保护区的监测和保护、应对气候变化和自然灾害等方面，提供技术服务，并通过教育培训，增强其空间技术方面的能力建设，促进可持续发展。
						</span><span style="font-family: 宋体; font-size: 12pt;"></span>
					</p>
					<p style="text-align: left; text-indent: 28.3pt;">
						<span style="font-family: 宋体; font-size: 12pt;"></span>
					</p>
					<p style="text-align: center; vertical-align: top;" align="center">
						<b><span style="font-family: 宋体; font-size: 12pt;">主要研究方向<span></span></span></b>
					</p>
					<p
						style="text-indent: -21pt; margin-left: 21pt; vertical-align: top;"
						align="left">
						<span style="font-family: 宋体; font-size: 12pt;">•<span
							style="line-height: normal; font-family: 'Times New Roman'; font-size: 7pt;">
						</span></span><span style="font-family: 宋体; font-size: 12pt;">自然与文化遗产空间影像与环境数据库建设：以卫星和航空遥感数据为基础，与地球、环境科学相关联，以网络为支撑，构建全球世界遗产地的遥感影像与环境数据库，实现对遗产地空间数据的一体化管理。<span></span></span>
					</p>
					<p
						style="text-indent: -21pt; margin-left: 21pt; vertical-align: top;"
						align="left">
						<span style="font-family: 宋体; font-size: 12pt;">•<span
							style="line-height: normal; font-family: 'Times New Roman'; font-size: 7pt;">
						</span></span><span style="font-family: 宋体; font-size: 12pt;">典型遗产地变化的空间动态监测研究：以“天<span>-</span>空<span>-</span>地”多尺度、多源遥感数据为基础，对具有代表性的世界自然与文化遗产地进行监测和评估，为遗产地管理部门提供决策参考。<span></span></span>
					</p>
					<p
						style="text-indent: -21pt; margin-left: 21pt; vertical-align: top;"
						align="left">
						<span style="font-family: 宋体; font-size: 12pt;">•<span
							style="line-height: normal; font-family: 'Times New Roman'; font-size: 7pt;">
						</span></span><span style="font-family: 宋体; font-size: 12pt;">全球变化与自然灾害对遗产地的影响研究：对极端气候地区和灾害地区的遗产地进行监测和评估，分析气候变化、自然灾害等对遗产地的影响。</span>
					</p>
					<p
						style="text-indent: -21pt; margin-left: 21pt; vertical-align: top;"
						align="left">
						<span
							style="line-height: 1.5; text-indent: -21pt; font-size: 12pt;">•<span
							style="line-height: normal; font-family: 'Times New Roman'; font-size: 7pt;">
						</span></span><span
							style="line-height: 1.5; text-indent: -21pt; font-size: 12pt;">大型遗产地的情景模拟与重建：对长城、大运河、丝绸之路等大型遗产地的三维可视化展示。</span>
					</p>
					<p
