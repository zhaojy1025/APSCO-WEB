						class="MsoNormal" align="left">
						<span style="font-family: 宋体; font-size: 12pt;">中心实行理事会领导下的主任负责制，理事会为决策机构，科学委员会为咨询机构，下设秘书处与研究和技术两个中心、九个部。<span
